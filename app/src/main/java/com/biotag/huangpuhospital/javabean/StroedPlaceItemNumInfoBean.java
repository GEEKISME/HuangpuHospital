package com.biotag.huangpuhospital.javabean;

public class StroedPlaceItemNumInfoBean {
    private String id;
    private String placename;
    private String itemtotalnumber;
    private String itemcheckednumber;
    private String itemuncheckednumber;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPlacename() {
        return placename;
    }

    public void setPlacename(String placename) {
        this.placename = placename;
    }

    public String getItemtotalnumber() {
        return itemtotalnumber;
    }

    public void setItemtotalnumber(String itemtotalnumber) {
        this.itemtotalnumber = itemtotalnumber;
    }

    public String getItemcheckednumber() {
        return itemcheckednumber;
    }

    public void setItemcheckednumber(String itemcheckednumber) {
        this.itemcheckednumber = itemcheckednumber;
    }

    public String getItemuncheckednumber() {
        return itemuncheckednumber;
    }

    public void setItemuncheckednumber(String itemuncheckednumber) {
        this.itemuncheckednumber = itemuncheckednumber;
    }
}
