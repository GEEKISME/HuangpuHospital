package com.biotag.huangpuhospital.javabean;

public class AssetDetailBean {
    private String assetSerialNum;
    private String assetName;
    private String checkOrUncheck;

    public String getAssetSerialNum() {
        return assetSerialNum;
    }

    public void setAssetSerialNum(String assetSerialNum) {
        this.assetSerialNum = assetSerialNum;
    }

    public String getAssetName() {
        return assetName;
    }

    public void setAssetName(String assetName) {
        this.assetName = assetName;
    }

    public String getCheckOrUncheck() {
        return checkOrUncheck;
    }

    public void setCheckOrUncheck(String checkOrUncheck) {
        this.checkOrUncheck = checkOrUncheck;
    }
}
